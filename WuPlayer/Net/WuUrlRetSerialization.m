FOUNDATION_EXPORT NSArray * WUQueryStringPairsFromKeyAndValue(NSString *key, id value);

NSString * WUQueryStringFromParameters(NSDictionary *parameters) {
    NSMutableArray *mutablePairs = [NSMutableArray array];
    for (WUQueryStringPair *pair in WUQueryStringPairsFromDictionary(parameters)) {
        [mutablePairs addObject:[pair UrlEncodedStringValue]];
    }
    return [mutablePairs componentsJoinedByString:@"&"];
}

NSArray * WUQueryStringPairsFromDictionary(NSDictionary *dictionary) {
    return WUQueryStringPairsFromKeyAndValue(nil, dictionary);
}

NSArray * WUQueryStringPairsFromKeyAndValue(NSString *key, id value) {
    NSMutableArray *mutableQueryStringComponents = [NSMutableArray array];
    @try {
        
           NSSortDescriptor *sortDescriptor = [NSSortDescriptor sortDescriptorWithKey:@"description" ascending:YES selector:@selector(compare:)];

           if ([value isKindOfClass:[NSDictionary class]]) {
               NSDictionary *dictionary = value;
               // Sort dictionary keys to ensure consistent ordering in query string, which is important when deserializing potentially ambiguous sequences, such as an array of dictionaries
               for (id nestedKey in [dictionary.allKeys sortedArrayUsingDescriptors:@[ sortDescriptor ]]) {
                   id nestedValue = dictionary[nestedKey];
                   if (nestedValue) {
                       [mutableQueryStringComponents addObjectsFromArray:WUQueryStringPairsFromKeyAndValue((key ? [NSString stringWithFormat:@"%@[%@]", key, nestedKey] : nestedKey), nestedValue)];
                   }
               }
           } else if ([value isKindOfClass:[NSArray class]]) {
               NSArray *array = value;
               for (id nestedValue in array) {
                   [mutableQueryStringComponents addObjectsFromArray:WUQueryStringPairsFromKeyAndValue([NSString stringWithFormat:@"%@[]", key], nestedValue)];
               }
           } else if ([value isKindOfClass:[NSSet class]]) {
               NSSet *set = value;
               for (id obj in [set sortedArrayUsingDescriptors:@[ sortDescriptor ]]) {
                   [mutableQueryStringComponents addObjectsFromArray:WUQueryStringPairsFromKeyAndValue(key, obj)];
               }
           } else {
               [mutableQueryStringComponents addObject:[[WUQueryStringPair alloc] initWithField:key value:value]];
           }
           return mutableQueryStringComponents;
    } @catch (NSException *exception) {
        return mutableQueryStringComponents;
    }
    
   
}

