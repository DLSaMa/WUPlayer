- (NSString *)UrlEncodedStringValue {
    if (!self.value || [self.value isEqual:[NSNull null]]) {
        return WUPercentEscapedStringFromString([self.field description]);
    } else {
        return [NSString stringWithFormat:@"%@=%@", WUPercentEscapedStringFromString([self.field description]), WUPercentEscapedStringFromString([self.value description])];
    }
}

